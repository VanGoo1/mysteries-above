package me.vangoo.domain.contracts;

/** Умова контракту Sun (Seq 6, "Магічне засвідчення"). Параметри кожної умови — див. {@link Contract}. */
public enum ContractTerm {
    /** Сторони не атакують одна одну. Порушник карається Божественною карою. */
    PEACE,
    /**
     * {@code partyB} зобов'язується виконати дію (напр. доставити предмети) до дедлайну.
     * У UI — «Клятва»; параметри {@code amount}/{@code deadlineEpochMillis} (+ опційний
     * опис {@code item}). Виявлення виконання — майбутня квест-інтеграція; наразі порушення
     * настає лише по спливанню дедлайну.
     */
    DEBT
}
